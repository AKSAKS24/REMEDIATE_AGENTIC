from typing import Optional
from pydantic import BaseModel, Field


class RemediationRequest(BaseModel):
    pgm_name: str
    inc_name: str
    type: str
    name: Optional[str] = ""
    class_implementation: Optional[str] = ""
    code: str = Field(default="", description="ABAP code to be remediated")


class RemediationResponse(BaseModel):
    pgm_name: str
    inc_name: str
    type: str
    name: Optional[str] = ""
    class_implementation: Optional[str] = ""
    original_code: str
    remediated_code: str